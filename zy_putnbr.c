#include "ft_printf.h"

void	zy_putnbr(long long n)
{
	if (n < 0)
	{
		zy_putchar('-');
		n *= -1;
	}
	if (n > 9)
	{
		zy_putnbr(n / 10);
		zy_putnbr(n % 10);
	}
	if (n < 10)
		zy_putchar(n + 48);
}
